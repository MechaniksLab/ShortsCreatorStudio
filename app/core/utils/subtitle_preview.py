def _build_preview_word_timestamps(text: Optional[str], start_ms: int, end_ms: int) -> list[dict]:
    """Строит простые word timestamps для предпросмотра эффектов karaoke."""
    raw = (text or "").strip()
    if not raw:
        return []

    # Для языков с пробелами — по словам, иначе — по символам.
    parts = raw.split()
    if len(parts) <= 1:
        parts = [ch for ch in raw if not ch.isspace()]
    if not parts:
        return []

    duration = max(1, int(end_ms - start_ms))
    step = max(10, duration // len(parts))
    result = []
    cursor = int(start_ms)
    for i, token in enumerate(parts):
        token_start = cursor
        token_end = int(end_ms) if i == len(parts) - 1 else min(int(end_ms), token_start + step)
        if token_end <= token_start:
            token_end = min(int(end_ms), token_start + 10)
        result.append(
            {
                "text": token,
                "start_time": token_start,
                "end_time": token_end,
            }
        )
        cursor = token_end
    return result


def _split_preview_tokens(text: Optional[str]) -> list[str]:
    raw = (text or "").strip()
    if not raw:
        return []
    parts = [p for p in raw.split() if p]
    if len(parts) <= 1:
        parts = [ch for ch in raw if not ch.isspace()]
    return parts


def _build_word_by_word_dialogue(
    text: Optional[str],
    style_name: str,
    total_ms: int,
) -> list[str]:
    tokens = _split_preview_tokens(text)
    if not tokens:
        return []

    duration = max(300, int(total_ms))
    step = max(120, duration // max(1, len(tokens)))
    lines = []
    cursor = 0
    for i, token in enumerate(tokens):
        start_ms = min(duration - 1, cursor)
        end_ms = duration if i == len(tokens) - 1 else min(duration, start_ms + step)
        if end_ms <= start_ms:
            end_ms = min(duration, start_ms + 120)

        start = f"0:00:{start_ms // 1000:02d}.{(start_ms % 1000) // 10:02d}"
        end = f"0:00:{end_ms // 1000:02d}.{(end_ms % 1000) // 10:02d}"
        lines.append(f"Dialogue: 0,{start},{end},{style_name},,0,0,0,,{token}")
        cursor = end_ms
    return lines


def _ass_time_from_ms(ms: int) -> str:
    safe = max(0, int(ms))
    return f"0:00:{safe // 1000:02d}.{(safe % 1000) // 10:02d}"

